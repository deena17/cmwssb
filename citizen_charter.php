        <h4>RAINWATER HARVESTING</h4> 
        <p>There is one axiom that “Water Conserved is Water Produced”. True to this, the Board has been taking pioneering steps for implementation of Rain Water Harvesting and thereby enabling conserving and saving rain water.   The Rainwater Harvesting helps the Board and the Consumers in the following ways:</p>
        <ul class="listoption3">
                    <li>To avoid water scarcity.</li>
                    <li>To augment ground water storage.</li>
                    <li>To avoid water table depletion.</li>
                    <li>To improve ground water quality.</li>
                    <li>To avoid sea water intrusion in coastal areas.</li>
                    <li>To mitigate flood water during monsoon.</li>
                    <li>To minimize the formation of cracks in buildings, in clay formation areas.</li>
        </ul><br>
        
        <h5><strong>9.1.	Rainwater Harvesting methods</strong></h5>
		<p>The rain water harvesting structures have to be installed, based on soil conditions.   In Greater Chennai City three different soil conditions exist, namely, a) Sandy area. b)  Clay area and c)Hard rock area.  The methods or procedures to be adopted for each type of soil are narrated below”</p>
        <p><u>A) for sandy area.</p></u>
         <h5><strong>1) Percolation pit</strong></h5>
        
                    <p>- constructed in the open space at required intervals.</p>
                    <p>- Size 1mx1mx1.5m( depth)</p>
                    <p>- They may be square /rectangular/ circular in shape </p>
					<p>- Filled with sand at the bottom and  pebbles/ gravels on above the land</p>
					<p>-Covered with cement slab. </p>

		<h5><strong> 2) Recharge well</strong></h5>
                    <p>- size 3 feet dia. with 10 to 15 feet depth</p>
                    <p>- constructed with cement rings</p>
                    <p>- side walls must be perforated </p>
					<p>- bottom one foot to be filled with gravels/ pebbles.</p>
					<p>- Covered with cement slab. </p>

		<p><u> B) for Clay area.</p></u>
		        
        <h5><strong>1) Percolation pit with bore hole:</strong></h5>
					<p>- In addition to the percolation pit a bore hole has to drilled at the centre.</p>
                    <p>- constructed in the open space at required intervals.</p>
                    <p>- Pit size:  1mx1mx1.5m( depth) and bore hole size 150 – 300 mm dia. with 15 to 20 feet depth</p>
					<p>- They may be square /rectangular/ circular in shape.</p>
					<p>- Filled with pebbles/ gravels. </p>
					<p>- Covered with cement slab. </p>


		<h5><strong> 2) Recharge well</strong></h5>
                    <p>- size 3 feet dia. with 10 to 15 feet depth</p>
                    <p>- constructed with cement rings</p>
                    <p>- side walls must be perforated </p>
					<p>- Bottom one feet to be filled with gravels/ pebbles.</p>
					<p>- Covered with cement slab.</p>

		<p><u>C) for Hard rock  area.</p></u>
		        
        <h5><strong>1) Percolation pit:</strong></h5>
					<p>- constructed in the open space at required intervals.</p>
                    <p>- Size 1mx1mx1.5m( depth).</p>
                    <p>- They may be square /rectangular/ circular in shape</p>
					<p>- Filled with sand at the bottom and  pebbles/ gravels at the top.</p>
					<p>- Covered with cement slab. </p>

		<h5><strong> 2) Recharge well</strong></h5>
                    <p>- size 3 feet dia. with 10 to 15 feet depth</p>
                    <p>- constructed with cement rings</p>
                    <p>- side walls must be perforated </p>
					<p>- Bottom one feet to be filled with gravels/ pebbles.</p>
					<p>- Covered with cement slab.</p>
		
		   
         <h5><strong>9.2.	For the existing open well:</strong></h5>
                  <p>Apart from these, the rain water can be harvested in the existing open wells through a filter chamber.</p>
				  <p>- Water collected has to be sent to the top of the filter chamber</p>
                  <p>- Filter chamber size. 1mx1mx1.5 depth.</p>
                  <p>- Filled with sand at the bottom and pebbles / gravels at the top .</p>
                  <p>- Covered with cement slab</p>
        <h5><strong>Rain Water Harvesting – Maintenance Methods:</strong></h5>
        <ul class="listoption3">
                    <li>Rainwater may be harvested in tanks / sumps and in open wells during rain..</li>
                    <li>The terrace should be maintained clean for rainwater harvesting.</li>
                    <li>The materials used for filtering rainwater should be cleaned properly.</li>
                    <li>After draining the initial flow of dirty rainwater for a few minutes and good quality water has to be collected for use.</li>
					<li>For drinking purpose the collected water should be boiled.</li>
					<li>The tank / sump / vessels used for collecting rainwater have to be cleaned and covered properly.</li>
					<li>Proper disinfection should be done while storing rainwater. (5 gm. of bleaching powder may be added to 1000 lit. of water).</li>
					
			</ul><br>
        
        <h5><strong><u><b>9.4.	Provisions in the Chennai Metropolitan Area Ground Water(Regulation) Act 1987 and the Chennai Metropolitan Area Ground Water (Regulation) Rules,1988.:</strong></h5></b></u>
        <ul class="listoption3">
                    <p>9.4.1.	The Ground Water Regulation Act 27/1987, asamendedby the Act No. 37 of 2002 dated 24.10.2002stipulates that is mandatory to have and maintain Rainwater Harvesting structures in all premises including the existing ones.</p>
                    <p>9.4.2.	For guidance or assistance for installation of Rainwater Harvesting structure, Consumers shall contact the Depot Offices / Area Offices concerned.   The consumers can also contact the Rainwater Harvesting Cell at Head office, No.1, Pumping Station Road, Chintadripet, Chennai - 600 002 by Telephone No.28454080(direct) and 28451300, Extn. No.295</p>
			
			 <h4><b>10.	GREY WATER RECYCLING</h4></b>
                 <p>Rule 24 (A) of Chennai City Corporation Building Rules, 1972, has stipulated that in respect of Special/ Multi-storied Buildings, .there must be arrangements for separation of bath and wash basin water and for reuse of such water  Accordingly the Board had issued a Circular vide CIR. No. CMWSSB/MDO/SPL/2017 dated 20.04.17 for implementation of this procedure. Accordingly, the respective Field Engineers of the Board shall advice the consumer to make such provisions for separation of bath and wash basin water. Upon separation, there must be adequate treatment facility to treat such water and to keep it for exclusive use of toilet flushing in their building.  Such quantity of excess waste water not reused for toilet flushing had to be suitably connected to the rain water recharge structure for ground water recharge. </p>

