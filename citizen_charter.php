            <h5><strong>1.	PREAMBLE</strong></h5>
        <p>1.1.	Sustainable improvement of water supply and sewerage disposal services for Greater Chennai City is the foremost objective of Chennai Metropolitan Water Supply & Sewerage Board.  The Board visualizes that besides its mandate, awareness among the citizens as regards the water supply and sewerage system is of paramount importance CMWSS Board published a Citizen Charter in the year 1998 with the approval of the Government of Tamilnadu through their Order No.58, Municipal Administration and Water Supply Department, dated 16th April 1998. The changed scenario subsequent to publication of that Charter, warranted preparation and publication of a Revised and Expanded Citizen Charter.  This Charter basically covers (a) the details of services rendered by the Board in respect of water supply and sewerage system, (b) the Rights of the Citizen and (c) their Responsibilities with regard to the Systems which serves their cause.</p>
        <p>1.2.	The Citizen's Charter attempts to clearly state the standards of service delivery and for developing a completely transparent administration by taking the consumer into confident. As the services of the CMWSSB are in conformity with the declared standards, certain service standards are further revised and included in the Citizen's Charter, 2021.</p>
        <p>2.	<b>DEFINITIONS:</b>  Definitions for some of the terms used in this Charter are provided as below:</p>
        <p>2.1.	Added Areas: Added Areas means and includes those local bodies – panchayats and municipalities in the urban adjacent area of Chennai City annexed to form the Greater Chennai City.</p>
        <p>2.2.	Board :   The Board means the Chennai Metropolitan Water Supply and Sewerage Board (CMWSSB) and includes its Head Office, 15 Area Offices, 200 Depot Offices and other functional areas including Water Treatment Plants, Sewerage Treatment Plants, Water Distribution Station, Pumping Stations, Well Field, Training Centre and Quality Assurance Wingunder the control of Head Office.  Board is also represented as Metro Water.</p>
        <p>2.3.	Consumer :  Consumer means citizen of Greater Chennai City who has a water / sewerage connection managed by the Board and who pays the water/ sewerage tax and water/ sewerage charges within the prescribed time limit. It also includes those citizens residing within the operational jurisdiction of the Board.</p>
        <p>2.4.	Complaints:  Complaint means a written statement of grievance presented either in person or through online method.  This also includes grievances expressed through telephone or mobile application.</p>
        <p>2.5.	Grievance : A grievance means certain level of deficiency in the services provided by the Board in respect of water supply and sewerage disposal. Defective water supply and sewerage system can also form part of grievances of the Consumer.</p>
        <p>2.6.	Operational Area:  Operational area means the operational jurisdiction vested with the Board for water supply and sewerage services within the geographical area of Greater Chennai City.</p>
        <p>2.7.	Payment Gateway:  This means the selected net banking Facility adopted by the Board for receipt of dues from the consumers for water supply and sewerage related services.  This will automatically appear in the payment screen for online payment.</p>
