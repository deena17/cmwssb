                    <li>The Consumer possess the Right to avail the services provided by the Board in respect of water supply and sewerage disposal facilities functioning in the Greater Chennai City.</li>
                    <li>By availing the services provided by the Board, the consumer has the right to lead a healthier and quality life .</li>
                    <li>The consumer has the right to seek any clarification  / advice from the Board in the matter of taxes and charges payable by them. </li>
					
        </ul><br>
		<h5><strong>11.2. Responsibility of the Consumers.:</strong></h5>
		<ul class="listoption3">
                    <li> * The foremost responsibility of the Consumer is to resort to timely payment of water and sewerage tax and the water charges payable by them.  This is because the Board needs adequate funds to spend for the services to be rendered..</li>
					<li> *  There is perennial shortage of adequate water in the source lakes serving the Greater Chennai City.  Therefore, the Consumers need to restrict the use of treated water for drinking and cooking purposes only.</li>
					<li> *  As part of water conservation efforts, the consumer shall install maintain rain water harvesting facilities for which guidelines are available with the Board.</li>
					<li> *  The Consumer shall inform the Board if they notice leaks or leaky service line on the city roads or storm water drain crossing.</li>
					<li> * Leaky plumbing systems in their premises must be rectified then and there. </li>
					<li> * The water sump and over-head tanks in their buildings must be periodically cleaned with disinfectant. </li>
					<li> * In the case of hand pumps, the consumer shall provide a way to drain the spilling flow. </li>
					<li> * The consumer shall reportreport to the Board  without fail any illegal direct motor sucking identified by them. </li>
					<li> * In respect of sewerage, the consumer shall provide gully, floor trap and gratings as per specific standards </li>
					<li> * Water is precious commodity and therefore misuse of water must be avoided. </li>
					<li> * Rainwater shall not be allowed to enter the sewer</li>
					<li> * Cloths, rags, broken crockery, cut pieces of vegetables / eatables etc, should  not be thrown into sewer system</li>
					<li> * Heavy chemicals / paints, cement etc should not be allowed to flow into sewer system</li>
					<li> * Puncturing of Machine holes and removal of Machine covers should not be permitted </li>
					<li> * Animal waste like cow dung etc. should not be allowed to enter into sewer directly or indirectly.</li>

